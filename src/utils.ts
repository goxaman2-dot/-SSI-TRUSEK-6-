export function looksLikeSSIJson(text: string): boolean {
  if (!text || typeof text !== 'string') return false;
  const trimmed = text.trim();
  if (!trimmed.startsWith('{') || !trimmed.endsWith('}')) return false;
  const lower = trimmed.toLowerCase();
  const keys = ['"u1"', '"u2"', '"name"', '"author"', '"e1"', '"k1"', '"t1"'];
  return keys.filter(k => lower.includes(k)).length >= 2;
}

export function parseSSIJson(text: string): Partial<StartupData> | null {
  try {
    const parsed = JSON.parse(text);
    const data: Partial<StartupData> = {};
    const stringKeys: (keyof StartupData)[] = ['name', 'author', 'expert'];
    const numKeys: (keyof StartupData)[] = [
      'u1', 'u2', 'e1', 'e2', 'r1', 'r2', 'k1', 'k2', 't1', 't2', 's1', 's2', 'tam', 'sam', 'som', 'tav'
    ];

    stringKeys.forEach(k => {
      if (parsed[k] !== undefined) {
        (data as any)[k] = String(parsed[k]);
      }
    });

    numKeys.forEach(k => {
      if (parsed[k] !== undefined) {
        const val = parseFloat(parsed[k]);
        (data as any)[k] = isNaN(val) ? 0 : val;
      }
    });

    return data;
  } catch (e) {
    return null;
  }
}

export function validateStartupData(data: StartupData): DataWarning[] {
  const warnings: DataWarning[] = [];

  const addWarning = (field: keyof StartupData | 'market' | 'general', level: 'error' | 'warning', message: string, explanation: string) => {
    warnings.push({ field, level, message, explanation });
  };

  // 1. General checks
  if (!data.name || data.name.trim() === '') {
    addWarning('name', 'warning', 'Название стартапа не заполнено', 'Укажите понятное имя проекта для профессиональной верификации анкеты.');
  }
  if (!data.author || data.author.trim() === '') {
    addWarning('author', 'warning', 'ФИО автора не заполнено', 'Для корректного формирования отчета и сертификата укажите ФИО разработчика (стартапера).');
  }

  // 2. Numerical sign validation
  const keysToCheckNonNegative: (keyof StartupData)[] = [
    'u1', 'u2', 'e1', 'e2', 'r1', 'r2', 'k1', 'k2', 't1', 't2', 's1', 'tam', 'sam', 'som', 'tav'
  ];
  keysToCheckNonNegative.forEach(k => {
    if (typeof data[k] === 'number' && (data[k] as number) < 0) {
      addWarning(k, 'error', `Значение не может быть отрицательным`, 'Пожалуйста, введите корректное положительное число или ноль.');
    }
  });

  // 3. Factor U checks
  if (data.u2 <= 0) {
    addWarning('u2', 'error', 'Эпизодов потребности должно быть больше нуля', 'Потребность в продукте должна возникать хотя бы 1 раз в год.');
  } else if (data.u2 > 365) {
    addWarning('u2', 'warning', 'Необычно высокая частота потребности в год', 'Значение превышает 365 (ежедневно). Проверьте корректность заполнения эпизодов в году.');
  }

  // 4. Factor E checks
  if (data.e1 > 1440) {
    addWarning('e1', 'warning', 'Необычно высокая сессия взаимодействия (>24 часов)', 'Контакт за одну сессию указан в минутах. Убедитесь, что вы не ввели секунды или не завысили значение.');
  }
  if (data.e2 > 10) {
    addWarning('e2', 'warning', 'Крайне высокое соотношение цен к конкурентам', 'Ваш продукт стоит в 10 раз дороже конкурентов. Это существенно усложнит сбыт без кратного превосходства в качестве.');
  } else if (data.e2 === 0) {
    addWarning('e2', 'warning', 'Нулевое соотношение Наша цена / Цена конкурента', 'Если это коммерческий проект, у продукта должна быть ненулевая цена по отношению к рынку.');
  }

  // 5. Factor R checks
  if (data.r2 < 1 && data.r2 > 0) {
    addWarning('r2', 'error', 'Соотношение LTV / CAC меньше единицы', 'Это означает, что стоимость привлечения клиента (CAC) превышает доход от него (LTV). Бизнес будет работать в убыток при росте масштабов.');
  } else if (data.r2 < 3 && data.r2 >= 1) {
    addWarning('r2', 'warning', 'Соотношение LTV / CAC ниже инвестиционного стандарта', 'Для устойчивых стартапов целевым считается LTV/CAC >= 3. Если LTV/CAC ниже 3, бизнес-модель уязвима к стоимости маркетинга.');
  } else if (data.r2 > 15) {
    addWarning('r2', 'warning', 'Подозрительно высокое соотношение LTV / CAC', 'LTV/CAC > 15 встречается крайне редко. Проверьте, не занижена ли стоимость CAC или не завышена ли ценность LTV.');
  }

  // 6. Factor K checks
  if (data.k1 > 100000) {
    addWarning('k1', 'warning', 'Очень высокие капитальные затраты', 'Стартовые затраты указаны в тысячах рублей. Проверьте масштаб (100 млн рублей) — нет ли здесь лишних нулей.');
  }
  if (data.k2 > 100) {
    addWarning('k2', 'error', 'Маржинальность не может превышать 100%', 'Чистая маржинальность не может быть физически выше 100%.');
  } else if (data.k2 > 95) {
    addWarning('k2', 'warning', 'Подозрительно высокая маржинальность (>95%)', 'Даже в IT маржинальность с учетом поддержки и инфраструктуры редко превышает 85-90%. Проверьте расчет юнит-экономики.');
  } else if (data.k2 < 5 && data.k2 > 0) {
    addWarning('k2', 'warning', 'Крайне низкая маржинальность (<5%)', 'Бизнес с низкой маржинальностью очень уязвим к любым колебаниям себестоимости и цен.');
  }

  // 7. Factor T checks
  if (data.t1 > data.t2) {
    addWarning('t1', 'error', 'Срок выхода на EBITDA+ больше срока окупаемости CAPEX', 'Проект не может полностью окупить стартовые вложения (T2) раньше, чем ежемесячные доходы превысят операционные расходы (T1).');
  }
  if (data.t2 > 120) {
    addWarning('t2', 'warning', 'Крайне долгий срок окупаемости инвестиций (>10 лет)', 'Для венчурных и инновационных проектов оптимальный срок возврата вложений составляет до 3-5 лет.');
  }

  // 8. Factor S checks
  if (data.s1 > 100) {
    addWarning('s1', 'error', 'Доля рекомендаций не может превышать 100%', 'Пожалуйста, введите корректный процент в диапазоне от 0% до 100%.');
  } else if (data.s1 > 90) {
    addWarning('s1', 'warning', 'Аномально высокая доля клиентов по рекомендации', 'Доля органических рекомендаций свыше 90% крайне редка на старте коммерческого бизнеса. Проверьте гипотезу.');
  }

  if (data.s2 < -100 || data.s2 > 100) {
    addWarning('s2', 'error', 'Индекс NPS должен быть в диапазоне от -100 до +100', 'Интегральный показатель удовлетворенности клиентов измеряется строго от -100% до +100%.');
  }

  // 9. Market checks (TAM, SAM, SOM, TAV)
  if (data.tam > 0) {
    if (data.sam > data.tam) {
      addWarning('sam', 'error', 'Объем SAM превышает объем TAM', 'Достижимый сегмент рынка (SAM) не может быть больше всего доступного рынка (TAM).');
    }
    if (data.som > data.sam) {
      addWarning('som', 'error', 'Объем SOM превышает объем SAM', 'Реально захватываемый объем рынка за 3 года (SOM) не может превышать весь достижимый сегмент рынка (SAM).');
    }
    if (data.som < data.tav && data.som > 0 && data.tav > 0) {
      addWarning('market', 'warning', 'Планируемый захват рынка (SOM) меньше порога автономии (TAV)', 'Ваш планируемый годовой объем продаж через 3 года меньше, чем порог выживаемости (TAV). Проект будет хронически убыточным без изменения планов продаж.');
    }
    if (data.som > data.sam * 0.5 && data.sam > 0) {
      addWarning('som', 'warning', 'Агрессивная плановая доля рынка (>50% от SAM)', 'Планировать захват более половины доступного рынка за 3 года крайне рискованно. Стандартный консервативный SOM равен 1-15% от SAM.');
    }
  }

  if ((data.tam > 0 || data.sam > 0 || data.som > 0) && data.tav === 0) {
    addWarning('tav', 'warning', 'Порог автономии (TAV) равен нулю', 'При заполнении рыночных показателей важно указать порог автономии TAV, иначе рыночный мультипликатор MEI не сможет адекватно скорректировать оценку.');
  }

  return warnings;
}
